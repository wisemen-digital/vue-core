export type PlainDateUnit
  = 'day' | 'days'
    | 'week' | 'weeks'
    | 'month' | 'months'
    | 'year' | 'years'

export type ReachablePlainDateUnit
  = 'week'
    | 'month'
    | 'year'

export type GetPlainDateUnit
  = 'day of month' | 'day'
    | 'week of month' | 'week'
    | 'month'
    | 'year'

export type DiffPlainDateUnit
  = 'days'
    | 'weeks'
    | 'months'
    | 'quarters'
    | 'years'
